def sumar_dos_enteros():
    primer_entero = int(input("Introduce el primer entero: "))

    segundo_entero = int(input("Introduce el segundo entero: "))
    resultado = primer_entero + segundo_entero
    print("El resultado de sumar", primer_entero, "y", segundo_entero, "es:", resultado)


sumar_dos_enteros()
